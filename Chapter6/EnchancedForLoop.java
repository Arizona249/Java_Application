public class EnchancedForLoop
{
	public static void main(String[]args)
	{
		int[]numbers={2,8,16,20,36};
		String[] names={"Clinton","Solomon","Delight","Prize","Denceny"};
		
		for(String element:names)//just like the for loop in python;
		{
			System.out.println(element);
		}
		
		
		for(int element:numbers)//just like the for loop in python;
		{
			System.out.println(element);
		}
	}
}